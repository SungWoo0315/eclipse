package chapter_02;

public class Variable1_45p {

    public static void main(String[] args) {

        int level;      // 변수이름 : level, 자료형 : int(정수) // 정수형 변수 level을 선언

        level = 10;     // 값 10을 level 변수에 대입.
                        // = 는 오른쪽값을 왼쪽에 대입한다는 의미.

        
        System.out.println(level);  // level변수의 값을 출력.

        
        // 1분 복습 45쪽
        int age;        // 정수형 변수 age를 선언.

        age = 33;       // 값 33을 age 변수에 대입.

        System.out.println("나이는 : " + age);     // age 변수의 값을 출력.

        
        // 프로그래밍에서는 변하는 값을 변수라고 한다.
        // 값을 저장하기위한 공간 => 변수
        // 어떤 형태의 자료를 저장할것인지 `자료형`을 정해주어야 한다.

        // 변수의 자료형을 선택하고, 이름정하는것 => '변수를 선언한다'

    }

}
