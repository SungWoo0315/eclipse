package chapter_05;

public class p131_Person {
        
        // 멤버변수.
        String name;        // String형 변수 name 선언       // 이름                  
        int height;         // int형 변수 height 선언        // 키              
        double weight;      // double형 변수 weight 선언     // 몸무게       
        char gender;        // char형 변수 gender 선언       // 성별        
        boolean married;    // boolean형 변수 married 선언   // 결혼여부         
                  
        
        /*
         * 멤버변수 선언할 때 
         * 
         * 기본자료형 (int, double 등등) 선언할 수도 있고,
         * 클래스형으로 선언할 수도 있다.
         * 
         * 클래스형이란?
         * 객체 자료형 또는 참조자료형.
         * 
         * 참조자료형으로 사용하는 클래스는 String, Date 와 같이 
         * 이미 JDK에서 제공하는것일수도 있고,
         * 개발자가 직접 만든 Student 나 Person 같은 클래스가 
         * 다른클래스에서 사용하는 멤버변수의 자료형이 될 수도 있다.
         */
}
