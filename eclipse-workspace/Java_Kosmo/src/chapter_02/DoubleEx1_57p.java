package chapter_02;

public class DoubleEx1_57p {

    public static void main(String[] args) {

        // 자바에서는 double형을 기본으로 사용.

        // float형 : 4바이트

        // double형 : 8바이트

        double dnum = 3.14;         // 변수 dnum선언하고 여기에 실수값 3.14 대입.
        float fnum = 3.14F;         // float형에는 식별자 `F`를 붙여주어야 한다.

        System.out.println(dnum);   // 변수 dnum 출력.
        System.out.println(fnum);   // 변수 fnum 출력.

    }

}
