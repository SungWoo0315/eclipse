package chapter_02;

public class BooleanEx_59p {

    public static void main(String[] args) {

        boolean isMarried = true;           // boolean형 변수 isMarried를 선언하고 true를 대입해서 초기화.

        System.out.println(isMarried);      // 변수 isMarried 출력.

        
        /*
         * boolean형 변수는 true, false 만 대입 할 수 있고, 결과값도 true, false 만 출력가능.
         */
    }

}
